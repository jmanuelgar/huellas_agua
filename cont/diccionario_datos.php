      <h4> SISTEMA DE DESVIACIÓN COMBINADA </h4>

<table class="table table-striped">
  <tbody>
   <tr>
    <td> <b> Notas generales </b> </td>
    <td style="text-align: justify;">
Esta capa ilustra los túneles del Sistema de Desviación Combinada, que se construyeron en los años 1930, junto con un 
serie de presas de la zona poniente de la Ciudad de México, con el objetivo de "desviar" el agua que anteriormente 
bajaban a la zona planicie del Valle de México. Estos túneles de interconexión facilita la transferencia de agua entre 
las presas, atrevasando las barrancas naturales. No todos se encuentran en funcion, tanto por azolve como invasiones que 
prohiben la entrada de agua a ellos. Sin embargo, sigue siendo muy importantes para el función del sistema de drenaje y son 
generalmente un eje desconocido del sistema de drenaje metropolitana.
  </td>
   </tr>
   <tr>
